void dance_playback(tap_dance_state_t *state, void *user_data) {
    switch (state->count) {
        case 1:
            tap_code(KC_MPLY); break;
        case 2:
            tap_code(KC_MNXT); break;
        case 3:
            tap_code(KC_MPRV); break;
    }
}

